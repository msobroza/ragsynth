def render_benchmarks_readme(entries: Mapping[str, Mapping[str, str]]) -> str:
    """Render ``data/benchmarks/README.md`` body from per-dataset provenance entries.

    Args:
        entries: ``{dataset_name: {"url", "sha256", "license_note"}}``,
            rendered in sorted dataset-name order for determinism.

    Returns:
        The full markdown text (deterministic given the same entries).
    """
    lines = [_README_HEADER]
    for dataset in sorted(entries):
        info = entries[dataset]
        lines.append(f"\n## {dataset}\n")
        lines.append(f"- Source URL: {info['url']}\n")
        lines.append(f"- Archive sha256: {info['sha256']}\n")
        lines.append(f"- License note: {info['license_note']}\n")
    return "".join(lines)


def write_benchmarks_readme(
    benchmarks_root: Path, name: str, url: str, sha256: str, license_note: str
) -> Path:
    """Record one dataset's provenance and (re)write ``data/benchmarks/README.md``.

    Provenance for every previously-fetched dataset is kept in a JSON
    sidecar (``.benchmarks_provenance.json``) so the README always lists
    every dataset fetched so far -- not just the most recent one -- and
    stays byte-deterministic (sorted dataset order) regardless of fetch
    order (spec01 D32).

    Args:
        benchmarks_root: ``data/benchmarks/`` (created if missing).
        name: Dataset name (``"fiqa"``/``"nfcorpus"``).
        url: Upstream source URL.
        sha256: Hex sha256 of the downloaded archive.
        license_note: Upstream license text found by the fetch script.

    Returns:
        The written ``README.md`` path.
    """
    benchmarks_root.mkdir(parents=True, exist_ok=True)
    provenance_path = benchmarks_root / _PROVENANCE_FILENAME
    provenance: dict[str, dict[str, str]] = (
        json.loads(provenance_path.read_text(encoding="utf-8")) if provenance_path.is_file() else {}
    )
    provenance[name] = {"url": url, "sha256": sha256, "license_note": license_note}
    provenance_path.write_text(
        json.dumps(provenance, ensure_ascii=False, sort_keys=True, indent=2) + "\n",
        encoding="utf-8",
    )
    readme_path = benchmarks_root / "README.md"
    readme_path.write_text(render_benchmarks_readme(provenance), encoding="utf-8")
    return readme_path


def download_benchmark(name: str, out_dir: Path) -> Path:
    """Download, extract, and record provenance for one BEIR benchmark zip (D32).

    Not unit-tested (network I/O) -- see this module's tests for the pure
    helpers this delegates to (:func:`sha256_of_file`,
    :func:`write_benchmarks_readme`).

    Args:
        name: Dataset name; must be a key of :data:`BENCHMARK_URLS`.
        out_dir: ``data/benchmarks/`` root; the archive is extracted to
            ``out_dir/<name>/raw/``.

    Returns:
        The extracted ``raw/`` directory path.

    Raises:
        KeyError: If ``name`` is not a known benchmark.
    """
    url = BENCHMARK_URLS[name]
    dataset_dir = Path(out_dir) / name
    raw_dir = dataset_dir / "raw"
    dataset_dir.mkdir(parents=True, exist_ok=True)

    zip_path = dataset_dir / f"{name}.zip"
    with (
        urllib.request.urlopen(url) as response,  # noqa: S310 -- hardcoded BEIR release URL
        zip_path.open("wb") as fh,
    ):
        shutil.copyfileobj(response, fh)

    digest = sha256_of_file(zip_path)

    with tempfile.TemporaryDirectory() as tmp:
        tmp_path = Path(tmp)
        with zipfile.ZipFile(zip_path) as archive:
            archive.extractall(tmp_path)
        extracted_dirs = [p for p in tmp_path.iterdir() if p.is_dir()]
        source = extracted_dirs[0] if len(extracted_dirs) == 1 else tmp_path
        if raw_dir.exists():
            shutil.rmtree(raw_dir)
        shutil.move(str(source), str(raw_dir))

    (raw_dir / _SOURCE_MARKER_FILENAME).write_text(digest + "\n", encoding="utf-8")
    write_benchmarks_readme(Path(out_dir), name, url, digest, LICENSE_NOTES[name])
    logger.info(f"fetched {name}: {url} sha256={digest} -> {raw_dir}")
    return raw_dir
