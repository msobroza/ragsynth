"""Shared contract + helpers for benchmark converters (spec01 §5, D32/D33).

Converters emit exactly the ``jsonl_loader`` schema (``chunks.jsonl`` /
``queries.jsonl`` / ``anchor_qrels.jsonl``); ``Chunk.create`` recomputes
content-hash ids at load time (``ragsynth.datasets.jsonl_loader.load_chunks``),
so converters build the *same* ``Chunk`` objects here -- same
``text``/``doc_id``/``metadata`` -- to re-key qrels to those ids before the
loader ever runs (spec01 §5).

This module also carries the ``scripts/fetch_benchmarks.py`` helpers (URL
table, license notes, sha256, README writing): the script itself is an
argument-parsing shell that is not unit-tested (network I/O), but every
helper it calls lives here and is.
"""

from __future__ import annotations

import hashlib
import json
import logging
import shutil
import tempfile
import urllib.request
import zipfile
from dataclasses import asdict, dataclass
from pathlib import Path
from typing import TYPE_CHECKING, Protocol

from ragsynth.domain import Chunk

if TYPE_CHECKING:
    from collections.abc import Iterable, Mapping

logger = logging.getLogger(__name__)

_SOURCE_MARKER_FILENAME = "SOURCE_SHA256"
_PROVENANCE_FILENAME = ".benchmarks_provenance.json"
_README_HEADER = (
    "# Benchmark data provenance\n\n"
    "Auto-generated by `scripts/fetch_benchmarks.py` -- do not hand-edit.\n"
)

#: Upstream BEIR release URLs (spec01 §15.1 default: plain urllib, no `beir` dep).
BENCHMARK_URLS: dict[str, str] = {
    "fiqa": "https://public.ukp.informatik.tu-darmstadt.de/thakur/BEIR/datasets/fiqa.zip",
    "nfcorpus": "https://public.ukp.informatik.tu-darmstadt.de/thakur/BEIR/datasets/nfcorpus.zip",
}

#: Best-known upstream license text per dataset (spec01 §4 table); the fetch
#: script's recorded text is authoritative, this is the documented default.
LICENSE_NOTES: dict[str, str] = {
    "fiqa": (
        "FiQA-2018 (Maia et al., WWW 2018 challenge) carries no formal license "
        "from the original challenge -- research use only. Distributed via the "
        "BEIR benchmark (Thakur et al., NeurIPS Datasets & Benchmarks 2021)."
    ),
    "nfcorpus": (
        "NFCorpus (Boteva et al., ECIR 2016) is derived from NutritionFacts.org "
        "content -- non-commercial research use only."
    ),
}


@dataclass(frozen=True)
class ConversionManifest:
    """Per-conversion record: counts, provenance, per-file integrity (spec01 §5)."""

    dataset: str
    n_chunks: int
    n_queries: int
    n_qrel_entries: int
    source_version: str
    license_note: str
    output_sha256: dict[str, str]


class BenchmarkConverter(Protocol):
    """Contract every dataset converter module satisfies (spec01 §5)."""

    name: str

    def convert(self, raw_dir: Path, out_dir: Path) -> ConversionManifest:
        """Convert one dataset's raw layout under ``raw_dir`` into ``out_dir``."""
        ...


# --------------------------------------------------------------------------
# Generic, dataset-agnostic helpers.
# --------------------------------------------------------------------------


def sha256_of_file(path: Path) -> str:
    """Return the hex sha256 digest of a file's bytes."""
    return hashlib.sha256(path.read_bytes()).hexdigest()


def write_jsonl(path: Path, rows: Iterable[Mapping[str, object]]) -> None:
    r"""Write one canonical JSON object per line: deterministic bytes.

    ``json.dumps(..., ensure_ascii=False, sort_keys=True)`` + ``\n`` per row,
    so converting the same raw dir twice produces byte-identical output
    (spec01 §5).
    """
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as fh:
        for row in rows:
            fh.write(json.dumps(row, ensure_ascii=False, sort_keys=True) + "\n")


def write_manifest(out_dir: Path, manifest: ConversionManifest) -> Path:
    """Write ``manifest.json`` as canonical JSON under ``out_dir``; return its path."""
    path = out_dir / "manifest.json"
    path.write_text(
        json.dumps(asdict(manifest), ensure_ascii=False, sort_keys=True, indent=2) + "\n",
        encoding="utf-8",
    )
    return path


def resolve_source_version(raw_dir: Path) -> str:
    """Return the fetch-recorded archive sha256, or a deterministic dir-content marker.

    ``scripts/fetch_benchmarks.py`` writes ``SOURCE_SHA256`` (the downloaded
    zip's hex sha256) into the extracted raw directory; converters running
    against that layout read it verbatim (``sha256:<hex>``). Hand-made
    fixtures -- and any ``raw_dir`` lacking that marker -- fall back to a
    content-derived marker: sha256 over the sorted ``(relative_path,
    file_sha256)`` pairs of every file under ``raw_dir`` (``dircontent:<hex>``),
    so ``source_version`` stays deterministic across repeat conversions and
    still changes if the raw bytes ever do (spec01 §5).
    """
    marker = raw_dir / _SOURCE_MARKER_FILENAME
    if marker.is_file():
        return f"sha256:{marker.read_text(encoding='utf-8').strip()}"
    parts = [
        f"{p.relative_to(raw_dir).as_posix()}:{sha256_of_file(p)}"
        for p in sorted(raw_dir.rglob("*"))
        if p.is_file()
    ]
    digest = hashlib.sha256("|".join(parts).encode("utf-8")).hexdigest()
    return f"dircontent:{digest}"


# --------------------------------------------------------------------------
# BEIR raw-layout parsing, shared by fiqa/nfcorpus (D33: both are 1:1 native
# chunking over the same corpus.jsonl/queries.jsonl/qrels/*.tsv shape).
# --------------------------------------------------------------------------


def read_beir_corpus(path: Path) -> list[dict[str, str]]:
    """Read a BEIR ``corpus.jsonl`` in file order (``_id``, ``title``, ``text``)."""
    rows: list[dict[str, str]] = []
    with path.open(encoding="utf-8") as fh:
        for line in fh:
            if not line.strip():
                continue
            row = json.loads(line)
            rows.append(
                {
                    "_id": str(row["_id"]),
                    "title": str(row.get("title", "")),
                    "text": str(row["text"]),
                }
            )
    return rows


def read_beir_queries(path: Path) -> list[dict[str, str]]:
    """Read a BEIR ``queries.jsonl`` in file order (``_id``, ``text``)."""
    rows: list[dict[str, str]] = []
    with path.open(encoding="utf-8") as fh:
        for line in fh:
            if not line.strip():
                continue
            row = json.loads(line)
            rows.append({"_id": str(row["_id"]), "text": str(row["text"])})
    return rows


def read_beir_qrels_dir(qrels_dir: Path) -> dict[tuple[str, str], int]:
    r"""Concatenate every ``*.tsv`` split under ``qrels_dir``, deduped by max score.

    Each file is a header row (``query-id\tcorpus-id\tscore``) followed by
    tab-separated rows. Files are visited in sorted filename order; the
    result does not depend on that order since the merge takes the max
    score per ``(query-id, corpus-id)`` pair regardless of which file it
    was seen in (spec01 §5: "concatenated ... dedup by (query,doc) keeping
    max score").
    """
    merged: dict[tuple[str, str], int] = {}
    for tsv_path in sorted(qrels_dir.glob("*.tsv")):
        lines = [ln for ln in tsv_path.read_text(encoding="utf-8").splitlines() if ln.strip()]
        for line in lines[1:]:  # skip the header row
            qid, cid, score = line.split("\t")
            key = (qid, cid)
            merged[key] = max(merged.get(key, int(score)), int(score))
    return merged


def build_chunks(corpus_rows: Iterable[Mapping[str, str]]) -> list[Chunk]:
    r"""Build the same ``Chunk`` objects the loader will build from emitted ``chunks.jsonl``.

    Chunk text = ``f"{title}\n\n{text}"`` when title is non-empty else
    ``text``; ``doc_id`` = upstream ``_id``; ``metadata.upstream_id`` =
    upstream ``_id`` (spec01 §5).
    """
    chunks = []
    for row in corpus_rows:
        title = row["title"]
        text = f"{title}\n\n{row['text']}" if title else row["text"]
        upstream_id = row["_id"]
        chunks.append(
            Chunk.create(text=text, doc_id=upstream_id, metadata={"upstream_id": upstream_id})
        )
    return chunks


def binarize_qrels(
    raw_qrels: Mapping[tuple[str, str], int],
    upstream_to_chunk_id: Mapping[str, str],
    *,
    threshold: int = 1,
) -> dict[str, dict[str, int]]:
    """Filter to ``score >= threshold``, map to grade 1, re-key ``corpus-id -> chunk_id``.

    Sub-threshold rows (e.g. NFCorpus grade-0 judgments) are dropped
    entirely rather than kept at grade 0 -- ``anchor_qrels.jsonl`` records
    gold sets only, matching every other qrel producer in this codebase
    (``steps/qrel_builder.py``). Rows whose ``corpus-id`` is not among the
    converted chunks are also dropped (defensive: a raw archive could in
    principle reference a trimmed doc) rather than raising.

    Args:
        raw_qrels: ``(query-id, corpus-id) -> score`` from
            :func:`read_beir_qrels_dir`.
        upstream_to_chunk_id: ``corpus-id -> chunk_id`` built from the same
            :class:`Chunk` objects the loader will construct.
        threshold: Minimum score kept (FiQA: already binary, D33; NFCorpus:
            grade >= 1, D33/§15.2).

    Returns:
        ``query-id -> {chunk_id: 1}``, omitting queries with no surviving
        relevant chunk.
    """
    out: dict[str, dict[str, int]] = {}
    for (qid, cid), score in raw_qrels.items():
        if score < threshold:
            continue
        chunk_id = upstream_to_chunk_id.get(cid)
        if chunk_id is None:
            logger.warning(f"qrel row references unknown corpus id {cid!r} for query {qid!r}")
            continue
        out.setdefault(qid, {})[chunk_id] = 1
    return out


def convert_beir_corpus_1to1(
    raw_dir: Path,
    out_dir: Path,
    *,
    dataset_name: str,
    license_note: str,
    binarize_threshold: int = 1,
) -> ConversionManifest:
    """Shared 1:1 BEIR converter body for fiqa/nfcorpus (D33: one chunk per document).

    Both datasets share this exact mechanical conversion; only ``name`` and
    ``license_note`` differ. FiQA's qrels are already binary, so
    ``binarize_threshold=1`` is a no-op restatement of "keep score >= 1";
    NFCorpus's graded qrels are genuinely reduced by the same filter
    (spec01 §5, D33).

    Args:
        raw_dir: Directory containing ``corpus.jsonl``, ``queries.jsonl``,
            and ``qrels/*.tsv`` (BEIR layout).
        out_dir: Destination directory for the three emitted jsonl files
            and ``manifest.json``.
        dataset_name: Recorded on the manifest (``"fiqa"``/``"nfcorpus"``).
        license_note: Recorded on the manifest verbatim.
        binarize_threshold: Minimum upstream score kept, mapped to grade 1.

    Returns:
        The :class:`ConversionManifest` written alongside the output.
    """
    raw_dir = Path(raw_dir)
    out_dir = Path(out_dir)

    corpus_rows = read_beir_corpus(raw_dir / "corpus.jsonl")
    query_rows = read_beir_queries(raw_dir / "queries.jsonl")
    raw_qrels = read_beir_qrels_dir(raw_dir / "qrels")

    chunks = build_chunks(corpus_rows)
    upstream_to_chunk_id = {c.metadata["upstream_id"]: c.chunk_id for c in chunks}
    qrels_by_query = binarize_qrels(raw_qrels, upstream_to_chunk_id, threshold=binarize_threshold)

    chunk_rows = [
        {"text": c.text, "doc_id": c.doc_id, "metadata": dict(c.metadata)} for c in chunks
    ]
    out_query_rows = [{"query_id": q["_id"], "text": q["text"]} for q in query_rows]
    qrel_rows = [
        {"query_id": q["_id"], "qrels": qrels_by_query[q["_id"]]}
        for q in query_rows
        if q["_id"] in qrels_by_query
    ]

    write_jsonl(out_dir / "chunks.jsonl", chunk_rows)
    write_jsonl(out_dir / "queries.jsonl", out_query_rows)
    write_jsonl(out_dir / "anchor_qrels.jsonl", qrel_rows)

    output_sha256 = {
        fname: sha256_of_file(out_dir / fname)
        for fname in ("chunks.jsonl", "queries.jsonl", "anchor_qrels.jsonl")
    }
    manifest = ConversionManifest(
        dataset=dataset_name,
        n_chunks=len(chunk_rows),
        n_queries=len(out_query_rows),
        n_qrel_entries=sum(len(row["qrels"]) for row in qrel_rows),
        source_version=resolve_source_version(raw_dir),
        license_note=license_note,
        output_sha256=output_sha256,
    )
    write_manifest(out_dir, manifest)
    return manifest


